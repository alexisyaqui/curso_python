import math as m

print("REDONDEO DE NUMEROS")
print(m.floor(4.99))

print("\n")
print("APROXIMACION DE NUMEROS")
print(m.ceil(4.01))

print("\n")
print("sumatoria de numeros con decimales")
n = [0.99999, 1, 2, 3]
print(m.fsum(n))

print("\n")
print("truncar una dato")
print(m.trunc(45.785))

print("\n")
print("potencia de un numero")
print(m.pow(2, 3))

print("\n")
print("raiz cuadrada de un numero")
print(m.sqrt(16))
print(m.sqrt(9))

print("\n")
print("valor de pi")
print(m.pi)
