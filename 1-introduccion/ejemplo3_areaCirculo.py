#Calcular el area de un Circulo.

#diametro = float(input("ingrese el perimetro del circulo: "))
radio = float(input("ingrese el perimetro del circulo: "))
pi = 3.1416

#formula es: A = pi * r^2

area = pi * (radio * radio)
perimetro = 2 * pi * radio
print("el area del Circulo es: ", area)
print("el perimetro del circulo es: ", perimetro)
