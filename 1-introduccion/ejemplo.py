#calcular cociente y el residuo de dos numeros enteros
#cociente, residuo y divisor
print("CALCULAR EL COCIENTE Y RESIDUO\n")

a = int(input("Ingrese el cociente>: "))
b = int(input("ingrese el divisor>: "))

cociente = a // b
residuo = a % b

print(f'el cociente es: ', cociente, ' ', ' el residuo es: ', residuo)